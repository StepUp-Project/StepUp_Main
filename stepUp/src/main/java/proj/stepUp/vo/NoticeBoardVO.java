package proj.stepUp.vo;

public class NoticeBoardVO {
	
	private int noticeIndex;
	private String noticeTitle;
	private String noticeCnt;
	private String noticeWdate;
	private int total;
	
	public int getNoticeIndex() {
		return noticeIndex;
	}
	public void setNoticeIndex(int noticeIndex) {
		this.noticeIndex = noticeIndex;
	}
	public String getNoticeTitle() {
		return noticeTitle;
	}
	public void setNoticeTitle(String noticeTitle) {
		this.noticeTitle = noticeTitle;
	}
	public String getNoticeCnt() {
		return noticeCnt;
	}
	public void setNoticeCnt(String noticeCnt) {
		this.noticeCnt = noticeCnt;
	}
	public String getNoticeWdate() {
		return noticeWdate;
	}
	public void setNoticeWdate(String noticeWdate) {
		this.noticeWdate = noticeWdate;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
}
