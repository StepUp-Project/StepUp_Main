package proj.stepUp.vo;

public class SlideVO {
	private int slideIndex;
	private int slideNum;
	private String slideFileOname;
	private String slideFileRname;
	private String slideUrl;

	public int getSlideIndex() {
		return slideIndex;
	}
	public void setSlideIndex(int slideIndex) {
		this.slideIndex = slideIndex;
	}
	public int getSlideNum() {
		return slideNum;
	}
	public void setSlideNum(int slideNum) {
		this.slideNum = slideNum;
	}
	public String getSlideFileOname() {
		return slideFileOname;
	}
	public void setSlideFileOname(String slideFileOname) {
		this.slideFileOname = slideFileOname;
	}
	public String getSlideFileRname() {
		return slideFileRname;
	}
	public void setSlideFileRname(String slideFileRname) {
		this.slideFileRname = slideFileRname;
	}
	public String getSlideUrl() {
		return slideUrl;
	}
	public void setSlideUrl(String slideUrl) {
		this.slideUrl = slideUrl;
	}
}
